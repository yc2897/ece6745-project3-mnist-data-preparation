#!/usr/bin/env python3
"""Convert weights_fixed.json and test_vectors.json to C headers."""
import argparse
import json
from pathlib import Path


def flatten(x):
    if isinstance(x, list):
        for e in x:
            yield from flatten(e)
    else:
        yield x


def fmt_array(name, values):
    vals = list(flatten(values))
    body = ", ".join(str(v) for v in vals)
    return f"static const int32_t {name}[{len(vals)}] = {{ {body} }};"


def gen_weights(json_path, out_path, bits):
    data = json.loads(Path(json_path).read_text())
    guard = f"PROJ3_MNIST_WEIGHTS_{bits}_H"
    lines = [
        "// AUTO-GENERATED by gen_headers_for_app.py -- do not edit",
        f"#ifndef {guard}",
        f"#define {guard}",
        "",
        "#include <stdint.h>",
        "",
        f"#define PROJ3_FRAC_BITS  {data['FRAC_BITS']}",
        f"#define PROJ3_RESOLUTION {data.get('RESOLUTION', '?')}",
        f"#define PROJ3_INPUT_DIM  {data.get('INPUT_DIM',  '?')}",
        f"#define PROJ3_HIDDEN_DIM {data.get('HIDDEN_DIM', '?')}",
        f"#define PROJ3_OUTPUT_DIM {data.get('OUTPUT_DIM', '?')}",
        "",
        fmt_array("proj3_W1", data["W1"]),
        fmt_array("proj3_b1", data["b1"]),
        fmt_array("proj3_W2", data["W2"]),
        fmt_array("proj3_b2", data["b2"]),
        "",
        "#endif",
        "",
    ]
    Path(out_path).write_text("\n".join(lines))


def gen_test_vectors(json_path, out_path, bits):
    data = json.loads(Path(json_path).read_text())
    vectors = data["vectors"]
    xs, logits, labels = [], [], []
    for v in vectors:
        xs.extend(v["x"])
        logits.extend(v["logits"])
        labels.append(v["label"])

    guard = f"PROJ3_MNIST_TEST_VECTORS_{bits}_H"
    lines = [
        "// AUTO-GENERATED by gen_headers_for_app.py -- do not edit",
        f"#ifndef {guard}",
        f"#define {guard}",
        "",
        "#include <stdint.h>",
        "",
        f"#define PROJ3_NUM_VECTORS {len(vectors)}",
        "",
        fmt_array("proj3_test_x", xs),
        fmt_array("proj3_test_logits", logits),
        fmt_array("proj3_test_labels", labels),
        "",
        "#endif",
        "",
    ]
    Path(out_path).write_text("\n".join(lines))


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--dir", default="8bits",
                    help="Directory containing weights_fixed.json and test_vectors.json")
    ap.add_argument("--out-dir", default=None,
                    help="Output directory (default: same as --dir)")
    args = ap.parse_args()

    in_dir = Path(args.dir)
    out_dir = Path(args.out_dir) if args.out_dir else in_dir

    weights_json = in_dir / "weights_fixed.json"
    vectors_json = in_dir / "test_vectors.json"

    bits = json.loads(weights_json.read_text())["WEIGHT_BITS"]

    out_dir.mkdir(parents=True, exist_ok=True)
    gen_weights(weights_json, out_dir / f"weights_{bits}.h", bits)
    gen_test_vectors(vectors_json, out_dir / f"test_vectors_{bits}.h", bits)
    print(f"Wrote weights_{bits}.h and test_vectors_{bits}.h to {out_dir}")


if __name__ == "__main__":
    main()
