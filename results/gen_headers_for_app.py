#!/usr/bin/env python3
"""Convert weights_fixed.json and test_vectors.json to C headers.

Output filenames are topology-versioned: weights_<R>x<H>.h, test_vectors_<R>x<H>.h.
Test vectors are emitted in two forms:
  - int32_t proj3_test_x[N*IN]            -- for the software baseline path
  - int8_t  proj3_test_x_packed[N][STRIDE] -- for the xcel xmem path,
    cache-line aligned and padded to a multiple of 16 bytes.
"""
import argparse
import json
from pathlib import Path


def flatten(x):
    if isinstance(x, list):
        for e in x:
            yield from flatten(e)
    else:
        yield x


def fmt_int32_array(name, values):
    vals = list(flatten(values))
    body = ", ".join(str(v) for v in vals)
    return f"static const int32_t {name}[{len(vals)}] = {{ {body} }};"


def fmt_int8_padded_2d(name, vectors_xs, in_dim):
    """Emit `int8_t name[N][stride]` with each row padded to a 16-byte multiple.

    Each row holds `in_dim` data bytes followed by zero-pad bytes.
    Stride is derived from PROJ3_IN_DIM in proj3-config.h, so no
    macro is emitted here (would clash with the consumer-side derivation).
    """
    stride = ((in_dim + 15) // 16) * 16
    n = len(vectors_xs)
    rows = []
    for v in vectors_xs:
        row = list(v) + [0] * (stride - in_dim)
        # Wrap signed int8 into [-128, 127]
        row = [((x + 128) % 256) - 128 for x in row]
        rows.append("{ " + ", ".join(str(b) for b in row) + " }")
    body = ",\n  ".join(rows)
    return (
        f"__attribute__((aligned(16)))\n"
        f"static const int8_t {name}[{n}][{stride}] = {{\n  {body}\n}};"
    )


def gen_weights(json_path, out_path, resolution, hidden):
    data = json.loads(Path(json_path).read_text())
    bits = data["WEIGHT_BITS"]
    guard = f"PROJ3_MNIST_WEIGHTS_{resolution}x{hidden}_H"
    lines = [
        "// AUTO-GENERATED by gen_headers_for_app.py -- do not edit",
        f"#ifndef {guard}",
        f"#define {guard}",
        "",
        "#include <stdint.h>",
        "",
        f"#define PROJ3_FRAC_BITS  {data['FRAC_BITS']}",
        f"#define PROJ3_RESOLUTION {data.get('RESOLUTION', resolution)}",
        f"#define PROJ3_INPUT_DIM  {data.get('INPUT_DIM',  resolution * resolution)}",
        f"#define PROJ3_HIDDEN_DIM {data.get('HIDDEN_DIM', hidden)}",
        f"#define PROJ3_OUTPUT_DIM {data.get('OUTPUT_DIM', 10)}",
        f"#define PROJ3_PRECISION  {bits}",
        "",
        fmt_int32_array("proj3_W1", data["W1"]),
        fmt_int32_array("proj3_b1", data["b1"]),
        fmt_int32_array("proj3_W2", data["W2"]),
        fmt_int32_array("proj3_b2", data["b2"]),
        "",
        "#endif",
        "",
    ]
    Path(out_path).write_text("\n".join(lines))


def gen_test_vectors(json_path, out_path, resolution, hidden):
    data = json.loads(Path(json_path).read_text())
    vectors = data["vectors"]
    in_dim = resolution * resolution
    xs_int32, logits, labels = [], [], []
    xs_per_vector = []
    for v in vectors:
        xs_int32.extend(v["x"])
        xs_per_vector.append(v["x"])
        logits.extend(v["logits"])
        labels.append(v["label"])

    guard = f"PROJ3_MNIST_TEST_VECTORS_{resolution}x{hidden}_H"
    lines = [
        "// AUTO-GENERATED by gen_headers_for_app.py -- do not edit",
        f"#ifndef {guard}",
        f"#define {guard}",
        "",
        "#include <stdint.h>",
        "",
        f"#define PROJ3_NUM_VECTORS {len(vectors)}",
        "",
        fmt_int32_array("proj3_test_x", xs_int32),
        "",
        fmt_int8_padded_2d("proj3_test_x_packed", xs_per_vector, in_dim),
        "",
        fmt_int32_array("proj3_test_logits", logits),
        fmt_int32_array("proj3_test_labels", labels),
        "",
        "#endif",
        "",
    ]
    Path(out_path).write_text("\n".join(lines))


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--dir", required=True,
                    help="Directory containing weights_fixed.json and test_vectors.json "
                         "(e.g. results/49x16_8bits)")
    ap.add_argument("--resolution", type=int, required=True,
                    help="Image side length, e.g. 7, 14, or 28")
    ap.add_argument("--hidden", type=int, required=True,
                    help="Hidden layer width, e.g. 8, 16, 32, 64, or 128")
    ap.add_argument("--out-dir", default=None,
                    help="Output directory (default: same as --dir)")
    args = ap.parse_args()

    in_dir = Path(args.dir)
    out_dir = Path(args.out_dir) if args.out_dir else in_dir

    weights_json = in_dir / "weights_fixed.json"
    vectors_json = in_dir / "test_vectors.json"

    out_dir.mkdir(parents=True, exist_ok=True)
    weights_name = f"weights_{args.resolution}x{args.hidden}.h"
    vectors_name = f"test_vectors_{args.resolution}x{args.hidden}.h"
    gen_weights(weights_json, out_dir / weights_name, args.resolution, args.hidden)
    gen_test_vectors(vectors_json, out_dir / vectors_name, args.resolution, args.hidden)
    print(f"Wrote {weights_name} and {vectors_name} to {out_dir}")


if __name__ == "__main__":
    main()
